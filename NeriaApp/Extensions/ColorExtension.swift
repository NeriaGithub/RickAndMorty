import Foundation
import UIKit.UIColor

extension UIColor {
    
    class var CustomGreen: UIColor {
        return UIColor(red: 124/255.0, green: 168/255.0, blue: 121/255.0, alpha: 1)
    }
    
    class var CustomYellow: UIColor {
        return UIColor(red: 250/255.0, green: 222/255.0, blue: 162/255.0, alpha: 1)
    }
}
